# operators - An operator is a symbol or keyword in Python used to perform operations on values or variables. It tells Python to do operations like add, compare, assign, or check values.

# arithmentic oeprators - basic mathematical calculations
'''
addition - '+'
subtraction - '-'
division - '/'
multiplication - '*'
floor division - '//'
modulus - '%'
exponent - '**'
'''

# assignment operators - used to assign values to variables
'''
= - assign
+= - add and assign
-= - subtract and assign
/= - divide and assign
*= - multiply and assign
//= - floor divide and assign
**= - exponent and assign
%= - modulus and assign
'''

# comparison operators - compares two values
'''
==
!=
>
>=
<
<=
'''

# Logical operators - combine multiple conditions and returns a Boolean
'''
and - true if both conditions are true
or - false if both conditions are false
not - reverse the input
'''

# Bitwise operator - performs operations on individual bits of binary representation of a number
'''
& - return 1 if both are 1
| - return 0 if both are 0
~ - -(binary + 1)
bitwise left - a << n shifts n bits to left and adds n zeros to the right end
bitwise right - a >> n shifts n bits to right and discards n bits in the right 
'''

# Membership operator - checks whether a value exists in a group
'''
'in' and 'not in'
'''

# Identity operator - checks whether two variables refer to the same memory location
'''
'is' and 'is not'
'''
